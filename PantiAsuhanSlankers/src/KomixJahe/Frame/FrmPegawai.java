       public void tampilkanData()
    {
        String [] kolom = {"ID", "Nama Pegawai", "Jenis Kelamin","Status Pegawai", "Status Pernikahan", "Alamat", "Telepon", "Jabatan"};
        ArrayList<Pegawai> list = new Pegawai().getAll();
        Object rowData[] = new Object[8];
        
        tblPetugas.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for(Pegawai pg : list)
        {
            rowData[0] = pg.getIdpegawai();
            rowData[1] = pg.getNama();
            rowData[2] = pg.getJeniskelamin();
            rowData[3] = pg.getStatuskepegawaian();
            rowData[4] = pg.getStatusperkawinan();
            rowData[5] = pg.getAlamat();
            rowData[6] = pg.getTelepon();
            rowData[7] = pg.getJabatan();
            
            ((DefaultTableModel)tblPetugas.getModel()).addRow(rowData);
        }
    }
    public void cari(String keyword){
        String[] kolom = {"ID", "Nama Pegawai", "Jenis Kelamin","Status Pegawai", "Status Pernikahan", "Alamat", "Telepon", "Jabatan"};
        ArrayList<Pegawai> list = new Pegawai().search(keyword);
        Object rowData[] = new Object[8];
        
        tblPetugas.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for(Pegawai p : list)
        {
            rowData[0] = p.getIdpegawai();
            rowData[1] = p.getNama();
            rowData[2] = p.getJeniskelamin();
            rowData[3] = p.getStatuskepegawaian();
            rowData[4] = p.getStatusperkawinan();
            rowData[5] = p.getAlamat();
            rowData[6] = p.getTelepon();
            rowData[7] = p.getJabatan();
            
            ((DefaultTableModel) tblPetugas.getModel()).addRow(rowData);
        }
 }

