    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        Pegawai pg = new Pegawai();
        pg.setIdpegawai(Integer.parseInt(idPegawai.getText()));
        pg.setNama(Nama.getText());
        pg.setJeniskelamin(jk.getText());
        pg.setStatuskepegawaian(StatusPegawai.getText());
        pg.setStatusperkawinan(StatusKawin.getText());
        pg.setAlamat(Alamat.getText());
        pg.setTelepon(Telp.getText());
        pg.setJabatan(Jabatan.getText());
        pg.save();
        idPegawai.setText(Integer.toString(pg.getIdpegawai()));
        tampilkanData();
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblPetugas.getModel();
        int row = tblPetugas.getSelectedRow();
        
        Pegawai pg = new Pegawai().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        pg.delete();
        kosongkanForm();
        tampilkanData();
    }//GEN-LAST:event_btnHapusActionPerformed

