    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        DefaultTableModel model = (DefaultTableModel)tblAnak.getModel();
        int row = tblAnak.getSelectedRow();

        AnakAsuh anakasuh = new AnakAsuh().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        anakasuh.delete();
        kosongkanForm();
        tampilkanData();
    }//GEN-LAST:event_btnHapusActionPerformed

    private void tblAnakMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAnakMouseClicked
        DefaultTableModel model = (DefaultTableModel)tblAnak.getModel();
        int row = tblAnak.getSelectedRow();
        AnakAsuh anakasuh = new AnakAsuh();
        
        anakasuh = anakasuh.getById(Integer.parseInt(model.getValueAt(row, 0).toString()));

        txtIdAnak.setText(String.valueOf(anakasuh.getIdanak()));
        cmbPegawai.getModel().setSelectedItem(anakasuh.getPegawai());
        txtNama.setText(anakasuh.getNamaanak());
        txtAlamat.setText(anakasuh.getAlamat());
        txtTglmasuk.setText(anakasuh.getTglmasuk());
        txtTglkeluar.setText(anakasuh.getTglkeluar());
    }//GEN-LAST:event_tblAnakMouseClicked

