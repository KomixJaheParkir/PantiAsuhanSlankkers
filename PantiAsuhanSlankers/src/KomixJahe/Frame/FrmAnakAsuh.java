    public void tampilkanData()
    {
        String[] kolom = {"ID Anak", "Pengasuh", "Nama Anak", "Alamat Asal", "Tgl Masuk", "Tgl Keluar"};
        ArrayList<AnakAsuh> list = new AnakAsuh().getAll();
        Object rowData[] = new Object[6];
        
        tblAnak.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        for(int i = 0; i < list.size(); i++)
        {
            rowData[0] = list.get(i).getIdanak();
            rowData[1] = list.get(i).getPegawai().getIdpegawai();
            rowData[2] = list.get(i).getNamaanak();
            rowData[3] = list.get(i).getAlamat();
            rowData[4] = list.get(i).getTglmasuk();
            rowData[5] = list.get(i).getTglkeluar();
            
            ((DefaultTableModel)tblAnak.getModel()).addRow(rowData);
        }
    }
    
    public void cari(String keyword)
    {
        String[] kolom = {"ID Anak", "Pengasuh", "Nama Anak", "Alamat Asal", "Tgl Masuk", "Tgl Keluar"};
        ArrayList<AnakAsuh> list = new AnakAsuh().search(keyword);
        Object rowData[] = new Object[6];
        
        tblAnak.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for(AnakAsuh anakasuh : list)
        {
            rowData[0] = anakasuh.getIdanak();
            rowData[1] = anakasuh.getPegawai().getNama();
            rowData[2] = anakasuh.getNamaanak();
            rowData[3] = anakasuh.getAlamat();
            rowData[4] = anakasuh.getTglmasuk();
            rowData[5] = anakasuh.getTglkeluar();
            
            ((DefaultTableModel)tblAnak.getModel()).addRow(rowData);
        }
    }
    
    public void tampilkanCmbPegawai()
    {
        cmbPegawai.setModel(new DefaultComboBoxModel(new Pegawai().getAll().toArray()));
    }

