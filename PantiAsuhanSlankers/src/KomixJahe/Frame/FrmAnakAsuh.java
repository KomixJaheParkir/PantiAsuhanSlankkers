    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        AnakAsuh anakasuh = new AnakAsuh();
        anakasuh.setIdanak(Integer.parseInt(txtIdAnak.getText()));
        anakasuh.setPegawai((Pegawai)cmbPegawai.getSelectedItem());
        anakasuh.setNamaanak("anak " + txtNama.getText());
        anakasuh.setAlamat(txtAlamat.getText());
        anakasuh.setTglmasuk(txtTglmasuk.getText());
        anakasuh.setTglkeluar(txtTglkeluar.getText());
        anakasuh.save();

        txtIdAnak.setText(Integer.toString(anakasuh.getIdanak()));
        tampilkanData();
    }//GEN-LAST:event_btnSimpanActionPerformed

